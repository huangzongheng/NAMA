from torch.utils.data import DataLoader

from .collate_batch import train_collate_fn, val_collate_fn, train_qg_collate_fn,\
    train_ema_collate_fn, train_attr_collate_fn
from .datasets import init_dataset, ImageDataset, ImageDatasetEma
from .samplers import RandomIdentitySampler, GlobalHardMiningSampler, RandomIdentitySampler_alignedreid  # New add by gu
from .transforms import build_transforms, build_qg_transforms, build_ema_transforms


def make_data_loader(cfg, use_attr=False, transforms_builder=build_transforms):
    train_transforms = transforms_builder(cfg, is_train=True)
    val_transforms = transforms_builder(cfg, is_train=False)
    # train_transforms = build_transforms(cfg, is_train=True)
    # val_transforms = build_transforms(cfg, is_train=False)
    num_workers = cfg.DATALOADER.NUM_WORKERS
    if True or len(cfg.DATASETS.NAMES) == 1:
        dataset = init_dataset(cfg.DATASETS.NAMES, root=cfg.DATASETS.ROOT_DIR, use_attr=use_attr, combine_all=cfg.DATASETS.COMBINE_ALL)
    # else:
    #     # TODO: add multi dataset to train
    #     dataset = init_dataset(cfg.DATASETS.NAMES, root=cfg.DATASETS.ROOT_DIR)

    num_classes = dataset.num_train_pids
    # if use_attr is True:        # 使用属性标签则使用对应的loader
    #     train_collate_fn = train_attr_collate_fn

    train_set = ImageDataset(dataset.train, train_transforms)
    if cfg.DATALOADER.SAMPLER == 'softmax':
        train_loader = DataLoader(
            train_set, batch_size=cfg.SOLVER.IMS_PER_BATCH, shuffle=True, num_workers=num_workers,
            collate_fn=(train_attr_collate_fn if use_attr else train_collate_fn)
        )
    elif cfg.DATALOADER.SAMPLER == 'g-hard':
        train_loader = DataLoader(
            train_set, batch_size=cfg.SOLVER.IMS_PER_BATCH,
            sampler=GlobalHardMiningSampler(dataset.train, cfg.SOLVER.IMS_PER_BATCH,
                                            cfg.DATALOADER.NUM_INSTANCE, cfg.DATALOADER.K),
            # sampler=RandomIdentitySampler_alignedreid(dataset.train, cfg.DATALOADER.NUM_INSTANCE),      # new add by gu
            num_workers=num_workers, collate_fn=(train_attr_collate_fn if use_attr else train_collate_fn)
        )
    else:
        train_loader = DataLoader(
            train_set, batch_size=cfg.SOLVER.IMS_PER_BATCH,
            sampler=RandomIdentitySampler(dataset.train, cfg.SOLVER.IMS_PER_BATCH, cfg.DATALOADER.NUM_INSTANCE, cfg.SEED),
            # sampler=RandomIdentitySampler_alignedreid(dataset.train, cfg.DATALOADER.NUM_INSTANCE),      # new add by gu
            num_workers=num_workers, collate_fn=(train_attr_collate_fn if use_attr else train_collate_fn)
        )

    val_set = ImageDataset(dataset.query + dataset.gallery, val_transforms)
    val_loader = DataLoader(
        val_set, batch_size=cfg.TEST.IMS_PER_BATCH, shuffle=False, num_workers=num_workers,
        collate_fn=val_collate_fn
    )
    return train_loader, val_loader, len(dataset.query), num_classes

